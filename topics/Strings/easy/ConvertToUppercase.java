class ConvertToUppercase {
   public static void main(String[] args) {
       String str = "hello";
       String res = convertToUppercase(str);
       System.out.println("Result => " + res);
   }

   public static String convertToUppercase(String str) {
       return str.toUpperCase();
   }
}


