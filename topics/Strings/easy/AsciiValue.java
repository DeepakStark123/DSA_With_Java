class AsciiValue {
    public static void main(String[] args) {
        // Input character
        char input = 'A';

        // Find the ASCII value
        int asciiValue = (int) input;

        // Output the result
        System.out.println("Input: '" + input + "'");
        System.out.println("Output: " + asciiValue);
    }
}



